     * Look up a specific color by name and returns the HEX.
     * If none, returns white.
     * @param $color
     * @return string
     */
    public static function lookUp($color)
    {
        if (isset(self::hslColors()[$color])) {
            return self::hslColors()[$color][0];
        }
        return '#fff';
    }

